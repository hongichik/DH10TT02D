# Tạo một list
Hoaqua = ["tao", "chuoi", "cam", "nho", "dua"]

# append(): thêm 
Hoaqua.append("Mango")  

# Thay đổi phần tử ở vị trí index = 1 (phần tử thứ 2 trong list, vì index bắt đầu từ 0)
Hoaqua[1] = "Mango"  

# pop(): xóa phần tử cuối cùng trong list và trả về giá trị vừa xóa
Hoaqua.pop()  

# In ra list sau tất cả các thao tác
print(Hoaqua)  
